#!/bin/bash
num1=15
num2=3

suma=$((num1 + num2))
resta=$((num1 - num2))
multiplicacion=$((num1 * num2))
division=$((num1 / num2))

echo "Numeros utilizados para operar: $num1 y $num2"
echo "Suma: $num1 + $num2 = $suma"
echo "Resta: $num1 - $num2 = $resta"
echo "Multiplicacion: $num1 * $num2 = $multiplicacion"
echo "Division: $num1 / $num2 = $division"
